class LogEntry:

    def __init__(self, v, i, n, d, ti, r, c, t, ic, s):
        self.visibility = v
        self.id = i
        self.name = n
        self.description = d
        self.timestamp = ti
        self.reference = r
        self.creator = c
        self.type = t
        self.icon = ic
        self.source = s
